def main():
    with open(LOCALES / "en.json", encoding="utf-8") as f:
        en = json.load(f)
    en_keys = {k: en[k] for k in en if k.startswith(("auth.signIn.forgot", "auth.forgot.", "auth.reset."))}

    for path in sorted(LOCALES.glob("*.json")):
        code = path.stem
        if code in ("en", "ar"):
            continue
        with open(path, encoding="utf-8") as f:
            data = json.load(f)
        trans = T.get(code, en_keys)
        changed = False
        for k, v in trans.items():
            if k not in data:
                data[k] = v
                changed = True
        if not changed:
            continue
        
        # preserve key order: merge new keys after auth.signIn.errPasswordRequired block
        with open(path, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2 if path.name != "zh.json" else 4)
            f.write("\n")

if __name__ == "__main__":
    main()
